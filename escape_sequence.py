# Escape Sequences in Python
print("1. New Line:\nThis text is on a new line.")           # \n inserts a new line
print("2. Tab:\tThis text is tabbed.")                       # \t inserts a horizontal tab
print("3. Backslash: This is a backslash: \\")               # \\ inserts a backslash
print("4. Single Quote: It\'s a single quote.")              # \' inserts a single quote
print("5. Double Quote: He said, \"Hello!\"")                # \" inserts a double quote
print("6. Carriage Return: Hello\rWorld")                    # \r moves cursor to beginning, then overwrites
print("7. Backspace: Hello\b!")                              # \b backspace deletes previous character
print("8. Form Feed: Hello\fWorld")                          # \f form feed (rarely used, acts like a page break)
print("9. Vertical Tab: Hello\vWorld")                       # \v inserts a vertical tab (rare)
print("10. Unicode Character: \u03A9")                       # \u prints Unicode character (e.g., Greek Omega)
print("11. Octal Value: \101")                               # \ooo — Octal value (prints 'A')
print("12. Hex Value: \x41")                                 # \xhh — Hex value (prints 'A')
